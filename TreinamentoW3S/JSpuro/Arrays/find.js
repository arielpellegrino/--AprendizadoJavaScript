// Definição e uso
// O método find() retorna o valor do primeiro elemento que passa em um teste.
// O método find() executa uma função para cada elemento do array.
// O método find() retorna undefined se nenhum elemento for encontrado.
// O método find() não executa a função para elementos vazios.
// O método find() não altera o array original.

// Parametros
// function() Obrigatório* :Uma função a ser executada para cada elemento do array.
// currentValue Obrigatório*: O valor do elemento atual.
// index Opcional*: O índice do elemento atual.
// arr Opcional*: A matriz do elemento atual.
// thisValue Opcional*: Padrão indefinido Um valor passado para a função como seu valor this.